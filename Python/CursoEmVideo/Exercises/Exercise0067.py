tabuada = int(input('Qual valor deseja ver a tabuada? '))
while True:
    if tabuada < 0:
        break
    print('{} X {} = {}'.format(0,tabuada,tabuada*0))
    print('{} X {} = {}'.format(1,tabuada,tabuada*1))
    print('{} X {} = {}'.format(2,tabuada,tabuada*2))
    print('{} X {} = {}'.format(3,tabuada,tabuada*3))
    print('{} X {} = {}'.format(4,tabuada,tabuada*4))
    print('{} X {} = {}'.format(5,tabuada,tabuada*5))
    print('{} X {} = {}'.format(6,tabuada,tabuada*6))
    print('{} X {} = {}'.format(7,tabuada,tabuada*7))
    print('{} X {} = {}'.format(8,tabuada,tabuada*8))
    print('{} X {} = {}'.format(9,tabuada,tabuada*9))
    print('{} X {} = {}'.format(10,tabuada,tabuada*10))
    tabuada = int(input('Qual valor deseja ver a tabuada? '))